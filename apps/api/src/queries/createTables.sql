CREATE TABLE IF NOT EXISTS regions (id INTEGER NOT NULL PRIMARY KEY, name TEXT NOT NULL);
CREATE TABLE IF NOT EXISTS communes (id INTEGER NOT NULL PRIMARY KEY, region_id INTEGER NOT NULL, name TEXT NOT NULL, FOREIGN KEY (region_id) REFERENCES regions(id));
CREATE TABLE IF NOT EXISTS usertypes (id INTEGER NOT NULL PRIMARY KEY, name TEXT NOT NULL);
CREATE TABLE IF NOT EXISTS users (id INTEGER NOT NULL PRIMARY KEY, name TEXT NOT NULL, email TEXT NOT NULL, password TEXT NOT NULL, phone_number TEXT NOT NULL, type_id INTEGER, webpage TEXT, description TEXT, is_verified INTEGER DEFAULT 0, FOREIGN KEY (type_id) REFERENCES usertypes(id));
CREATE TABLE IF NOT EXISTS species (id INTEGER NOT NULL PRIMARY KEY, name TEXT NOT NULL);
CREATE TABLE IF NOT EXISTS sizes (id INTEGER NOT NULL PRIMARY KEY, name TEXT NOT NULL);
CREATE TABLE IF NOT EXISTS sex (id INTEGER NOT NULL PRIMARY KEY, name TEXT NOT NULL);
CREATE TABLE IF NOT EXISTS pets (id INTEGER NOT NULL PRIMARY KEY, name TEXT, is_adopted INTEGER NOT NULL DEFAULT 0, is_exotic INTEGER NOT NULL, species_id INTEGER NOT NULL, size_id INTEGER NOT NULL, breed TEXT NOT NULL DEFAULT "No especificado", sex_id INTEGER NOT NULL, age REAL NOT NULL, description TEXT DEFAULT "No se ha proporcionado una descripción para este animalito", owner_id INTEGER NOT NULL, email TEXT NOT NULL, phone_number TEXT NOT NULL, region_id INTEGER NOT NULL, commune_id INTEGER NOT NULL, FOREIGN KEY (species_id) REFERENCES species(id), FOREIGN KEY (size_id) REFERENCES sizes(id), FOREIGN KEY (sex_id) REFERENCES sex(id), FOREIGN KEY (owner_id) REFERENCES users(id), FOREIGN KEY (region_id) REFERENCES regions(id), FOREIGN KEY (commune_id) REFERENCES communes(id));
CREATE TABLE IF NOT EXISTS images (id INTEGER NOT NULL PRIMARY KEY, pet_id INTEGER NOT NULL, url TEXT NOT NULL, FOREIGN KEY (pet_id) REFERENCES pets(id));


